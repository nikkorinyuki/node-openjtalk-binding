const fs = require("fs");

const d = fs.readFileSync("bep-eng.dic").toString().split("\n").filter(e => e.length > 1).map(e => e.split(" ")).sort((a, b) => a[0].length-b[0].length);
let csv = "";

for (let index = 0; index < d.length; index++) {
    const line = d[index];
    //csv += `${toFullWidth(kanaHalfToFull(line[0]))},${line.slice(1,9+1).join(",")},${toFullWidth(kanaHalfToFull(line[10]))},${line[11]},${line[12]},1/${line[12].length},*\n`;
    csv += `${toFullWidth(kanaHalfToFull(先頭大文字(line[0])))},,,${index + 1},名詞,一般,*,*,*,*,${toFullWidth(kanaHalfToFull(先頭大文字(line[0])))},${kanaHalfToFull(line[1])},${kanaHalfToFull(line[1])},1/${line[1].length},*\n`;
}

fs.writeFileSync("bep-eng.csv", csv);

function 先頭大文字(str) {
    return /*str.charAt(0).toUpperCase() +*/ str.slice(0).toLowerCase();
}
function toFullWidth(str) {
    // 半角英数字を全角に変換
    str = str.replace(/[A-Za-z0-9]/g, function (s) {
        return String.fromCharCode(s.charCodeAt(0) + 0xFEE0);
    });
    return str;
}

